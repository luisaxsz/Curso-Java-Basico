package com.mycompany.variaveis;

/**
 *
 * @author maria
 */
public class Variaveis {

    public static void main(String[] args) {
        byte idade01 = 20;
        short idade02 = 21;
        int idade03 = 67;
        long idade04 = 23;
        
        System.out.println("Idade 1: " + idade01);
        System.out.println("Idade 2: " + idade02);
        System.out.println("Idade 3: " + idade03);
        System.out.println("Idade 4: " + idade04);
        
        double valorPassagem = 2.50;
        float valorTomate = 3.45f;
        
        System.out.println("Valor passagem: " + valorPassagem);
        System.out.println("Valor tomate: " + valorTomate);
        
        char o = 111;
        char i = 105;
        
        System.out.println("" + o + i);
        
        boolean verdadeiro = true;
        boolean falso = false;
        
        System.out.println("Verdadeiro:\t" + verdadeiro);
        System.out.println("Falso:\n" + falso);
    }
}